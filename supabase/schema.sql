-- Bucket untuk gambar project
insert into storage.buckets (id, name, public)
values ('project-images', 'project-images', true)
on conflict do nothing;

-- Bucket untuk gambar certificate
insert into storage.buckets (id, name, public)
values ('certificate-images', 'certificate-images', true)
on conflict do nothing;

-- Bucket untuk foto profil komentar
insert into storage.buckets (id, name, public)
values ('profile-images', 'profile-images', true)
on conflict do nothing;

-- Storage policies: siapa saja bisa baca (public bucket)
create policy "Public read project-images"
  on storage.objects for select
  using (bucket_id = 'project-images');

create policy "Auth upload project-images"
  on storage.objects for insert
  with check (bucket_id = 'project-images' and auth.role() = 'authenticated');

create policy "Auth delete project-images"
  on storage.objects for delete
  using (bucket_id = 'project-images' and auth.role() = 'authenticated');

-- Certificate images
create policy "Public read certificate-images"
  on storage.objects for select
  using (bucket_id = 'certificate-images');

create policy "Auth upload certificate-images"
  on storage.objects for insert
  with check (bucket_id = 'certificate-images' and auth.role() = 'authenticated');

create policy "Auth delete certificate-images"
  on storage.objects for delete
  using (bucket_id = 'certificate-images' and auth.role() = 'authenticated');

